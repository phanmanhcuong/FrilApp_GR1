    private void loadAccount() {
        if (Settings.rememberme) {
            TextField_Username.setText(Settings.username);
            PasswordField.setText(Settings.password);
            CheckBox.setSelected(true);
        }
    }

    private void GetFrilHomePage() throws MalformedURLException, IOException {
        String formUrl = FRIL_SIGNIN; // NOTE: This is the URL the form POSTs to, not the URL of the form (you can find this in the "action" attribute of the HTML's form tag
        if ((true == Settings.rememberme) && !(Settings.cookie.isEmpty())) { //remember cookie
            HttpCookie httpCookie = new HttpCookie("_fril_user_session", Settings.cookie);
            httpCookie.setDomain("fril.jp");
            cookieStore.add(null, httpCookie);
            cookieId = Settings.cookie;
        } else {

        }
        authenticityToken = Utility.getToken(formUrl);
    }

