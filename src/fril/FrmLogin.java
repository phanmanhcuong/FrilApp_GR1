    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (true == (Settings.rememberme) && !((Settings.cookie).isEmpty())) { //remember cookie
            //go directly to product management
        }
        if (TextField_Username.getText().isEmpty()) {
            JOptionPane.showMessageDialog(jButton1, USERNAME_EMPTY, "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (0 == PasswordField.getPassword().length) {
            JOptionPane.showMessageDialog(this, PASSWORD_EMPTY, "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        username = TextField_Username.getText();
        password = new String(PasswordField.getPassword());

        try {
            Utility.logIn(FrmLogin.this, username, password);
        } catch (IOException ex) {
            Logger.getLogger(FrmLogin.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

